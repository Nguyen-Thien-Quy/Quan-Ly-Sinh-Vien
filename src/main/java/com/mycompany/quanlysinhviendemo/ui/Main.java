    private void fillToJTable() {
        if (data != null) {
            for (SinhVien s : data) {
                model.addRow(new Object[]{
                    stt++,
                    s.getHoTen(),
                    s.getMaSV(),
                    s.getLopNienChe(),
                    s.getNgaySinh(),
                    s.getQueQuan(),
                    s.shortName,
                    String.valueOf(s.tinhDiem())
                });
            }
        }
    }

