    private void mnuiMoFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuiMoFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Open a file");

        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("*.json", "json"));

        int value = fileChooser.showOpenDialog(this);
        if (value == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            FileReader fr = null;
            try {
                fr = new FileReader(file);
                Gson gson = new Gson();

                java.lang.reflect.Type type = new TypeToken<List<SinhVien>>() {
                }.getType();

                data = gson.fromJson(fr, type);
                fillToJTable();
                tbpMain.addTab("Quản lý sinh viên", manageSV);
                tbpMain.setSelectedComponent(manageSV);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(QuanLy.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                if (fr != null) {
                    try {
                        fr.close();
                    } catch (IOException ex) {
                        Logger.getLogger(QuanLy.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_mnuiMoFileActionPerformed

    private void mnuiLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuiLuuFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Save a file");

        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("*.json", "json"));

        int value = fileChooser.showSaveDialog(this);
        if (value == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            FileWriter fw = null;
            try {
                fw = new FileWriter(file);

                Gson gson = new Gson();
                gson.toJson(data, fw);
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                if (fw != null) {
                    try {
                        fw.flush();
                        fw.close();
                    } catch (IOException ex) {
                        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_mnuiLuuFileActionPerformed

